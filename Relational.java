package Operators;

public class Relational {
    public static void main(String[] args) {
        int a = 38;
        int b = 17;
        System.out.println(a < b);  
        System.out.println(a > b);  
        System.out.println(a <= b); 
        System.out.println(a >= b); 
    }
    
}
